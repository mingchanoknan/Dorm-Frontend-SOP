class Users {
    constructor(
      id,
      first_name,
      last_name,
      sex,
      birth_date,
      age,
      room_number,
      email,
      phone1,
      phone2,
      address,
      username,
      password,
    ) {
      this.id = id;
      this.first_name = first_name;
      this.last_name = last_name;
      this.sex = sex;
      this.birth_date = birth_date;
      this.age = age;
      this.room_number = room_number;
      this.email = email;
      this.phone1 = phone1;
      this.phone2 = phone2;
      this.address = address;
      this.username = username;
      this.password = password;
    }
  }

export default Users